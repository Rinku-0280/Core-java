class RationalConstructor
{
    public double sum(int a, int b, int c, int d)
	{
		return (a/b) + (c/d);
	}
    public double sub(int a, int b, int c, int d)
	{
		return (a/b) - (c/d);
	}
    public double mul(int a, int b, int c, int d)
	{
		return (a/b) * (c/d);
	}
    public double div(int a, int b, int c, int d)
	{
		return (a/b) / (c/d);
	}

}